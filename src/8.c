#define NUM_ADJACENT 13
#define ARR_SIZE(ARR) sizeof((ARR)) / sizeof((ARR)[0])


//---< main >-------------------------------------


int main(void)
{
    unsigned long greatest_prod = 0;

    for (size_t i = 0, size = ARR_SIZE(num) - 1; i < size; i++) {
        char tmp[NUM_ADJACENT] = { 0 };
        strncpy(tmp, &num[i], NUM_ADJACENT);

        unsigned long prod = 1;
        for (size_t j = 0; j < NUM_ADJACENT; j++) {

#if NUM_ADJACENT > 10
            if (tmp[j] == '0')
                break;
#endif

            prod *= (tmp[j] - '0');
        }

        if (prod > greatest_prod) {
            greatest_prod = prod;
        }
    }

    printf("Problem 008: %lu\n", greatest_prod);

    return 0;
}
